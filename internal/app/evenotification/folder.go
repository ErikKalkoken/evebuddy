var FolderTypes map[Folder][]Type

func init() {
	FolderTypes = make(map[Folder][]Type)
	for type_, folder := range Type2folder {
		c := folder
		if folder == Unknown {
			c = Miscellaneous
		}
		FolderTypes[c] = append(FolderTypes[c], type_)
	}
}
